import os
from helpers.helper_print import print_message, GREEN, CYAN


def generate_env(full_path):
    create_file_env(full_path)
    create_file_env_example(full_path)


def create_file_env(full_path):
    """
    Genera un archivo

    Args:
        full_path (str): Ruta completa del proyecto.
    """
    styles_path = os.path.join(full_path)

    # Crear la carpeta si no existe
    if not os.path.exists(styles_path):
        os.makedirs(styles_path)
        print_message(f"Carpeta creada: {styles_path}", GREEN)

    # Ruta completa del archivo
    file_path = os.path.join(styles_path, ".env")

    # Contenido por defecto
    content = r"""PORT=8080
DB_HOST=localhost
DB_DATABASE=example
DB_USER=root
DB_PASSWORD=123456
SECRETORPRIVATEKEY=ABC1234
"""

    try:
        # Crear o sobrescribir el archivo con el contenido
        with open(file_path, "w") as f:
            f.write(content)
        print_message(f"Archivo generado: {file_path}", GREEN)
    except Exception as e:
        print_message(f"Error al generar el archivo {file_path}: {e}", CYAN)






